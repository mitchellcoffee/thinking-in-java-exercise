package ch2;

public class Ex9 {

    public static void main(String[] args) {
        boolean b = false;
        char c = 'x';
        byte t = 8;
        short s = 16;
        int i = 32;
        long l = 64;
        float f = 0.32f;
        double d = 0.64;
        Boolean B = b;
        System.out.println(b);
        System.out.println(B);
        Character C = c;
        System.out.println(c);
        System.out.println(C);
        Byte T = t;
        System.out.println(t);
        System.out.println(T);
        Short S = s;
        System.out.println(s);
        System.out.println(S);
        Integer I = i;
        System.out.println(i);
        System.out.println(I);
        Long L = l;
        System.out.println(l);
        System.out.println(L);
        Float F = f;
        System.out.println(f);
        System.out.println(F);
        Double D = d;
        System.out.println(d);
        System.out.println(D);
    }

}
